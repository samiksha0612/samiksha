import java.io.*;
import java.util.*;
public class mathfunctionex{
public static void main(String args[]){
double num1=16.0;
double num2= -4.7;
double angledegrees=45.0;
System.out.println("Absolute value of" +num2 +":" +Math.abs(num2));
System.out.println("Square root of" +num1 +":" +Math.sqrt(num1));
System.out.println("4 raised to power of 2:" +Math.pow(4,2));
System.out.println("sin of" +angledegrees +"degrees:" +Math.sin(Math.toRadians(angledegrees)));
System.out.println("Random number:" +Math.random());
}
}

